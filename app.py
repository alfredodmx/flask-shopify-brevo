# üìå Funci√≥n para obtener la URL p√∫blica de la imagen desde el ID del archivo
def get_image_url_from_shopify(image_id):
    print(f"üîç Obteniendo URL para el archivo con ID: {image_id}")
    
    # URL de la API de archivos de Shopify
    shopify_url = f"https://{SHOPIFY_STORE}/admin/api/2023-10/files/{image_id}.json"
    
    headers = {
        "X-Shopify-Access-Token": SHOPIFY_ACCESS_TOKEN,
        "Content-Type": "application/json"
    }

    response = requests.get(shopify_url, headers=headers)
    
    if response.status_code == 200:
        file_data = response.json().get("file", {})
        print(f"üîç Datos del archivo: {file_data}")  # Depuraci√≥n de los datos del archivo
        file_url = file_data.get("url", "Sin URL")
        return file_url
    else:
        print("‚ùå Error obteniendo la URL del archivo de Shopify:", response.text)
        return "Sin URL"

